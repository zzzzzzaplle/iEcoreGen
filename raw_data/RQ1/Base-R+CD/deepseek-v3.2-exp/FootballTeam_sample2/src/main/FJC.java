import java.util.*;
import java.util.stream.Collectors;

/**
 * Represents a football team with players and announcements
 */
 class FootballTeam {
    private List<Player> firstEleven;
    private List<Player> spareTeam;
    private List<Player> players;
    private List<Announcement> announcements;

    /**
     * Default constructor initializing empty lists
     */
    public FootballTeam() {
        this.firstEleven = new ArrayList<>();
        this.spareTeam = new ArrayList<>();
        this.players = new ArrayList<>();
        this.announcements = new ArrayList<>();
    }

    // Getters and setters
    public List<Player> getFirstEleven() {
        return firstEleven;
    }

    public void setFirstEleven(List<Player> firstEleven) {
        this.firstEleven = firstEleven;
    }

    public List<Player> getSpareTeam() {
        return spareTeam;
    }

    public void setSpareTeam(List<Player> spareTeam) {
        this.spareTeam = spareTeam;
    }

    public List<Player> getPlayers() {
        return players;
    }

    public void setPlayers(List<Player> players) {
        this.players = players;
    }

    public List<Announcement> getAnnouncements() {
        return announcements;
    }

    public void setAnnouncements(List<Announcement> announcements) {
        this.announcements = announcements;
    }

    /**
     * Calculates the total number of goal-scoring announcements for the starting eleven players
     * Only announcements generated by forwards in the first eleven are counted
     *
     * @return total number of goal-scoring announcements by forwards in the first eleven
     */
    public int calculateTotalGoalScoringAnnouncementsForFirstEleven() {
        return (int) announcements.stream()
                .filter(announcement -> announcement.getType() == AnnouncementType.SCORE)
                .filter(announcement -> firstEleven.contains(announcement.getPlayer()))
                .filter(announcement -> announcement.getPlayer().getPosition() == Position.FORWARD)
                .count();
    }

    /**
     * Finds the midfielder player with the highest number within all players of the football team
     *
     * @return the midfielder with the highest number, or null if no midfielders found
     */
    public Player findMidfielderWithHighestNumber() {
        return players.stream()
                .filter(player -> player.getPosition() == Position.MIDFIELD)
                .max(Comparator.comparingInt(Player::getNumber))
                .orElse(null);
    }

    /**
     * Calculates the average age of players in the spare team
     *
     * @return the average age as a floating-point number, or 0.0 if the reserve team is empty
     */
    public double calculateAverageAgeOfSpareTeam() {
        if (spareTeam.isEmpty()) {
            return 0.0;
        }
        
        double sum = spareTeam.stream()
                .mapToInt(Player::getAge)
                .sum();
        return sum / spareTeam.size();
    }

    /**
     * Counts the number of goal-saving announcements made by all goalkeepers on the team
     * Only announcements generated by players whose position is GOALKEEPER are considered
     *
     * @return total number of goal-saving announcements by goalkeepers, 0 if no such announcements exist
     */
    public int calculateGoalSavingAnnouncementsByGoalkeeper() {
        return (int) announcements.stream()
                .filter(announcement -> announcement.getType() == AnnouncementType.SAVE)
                .filter(announcement -> announcement.getPlayer().getPosition() == Position.GOALKEEPER)
                .count();
    }

    /**
     * Calculates the total number of announcements made by all forwards on the team
     * Includes both score and save announcements made by forwards
     *
     * @return total number of announcements by forwards, 0 if there are no forwards or no announcements
     */
    public int calculateTotalAnnouncementsForForwardPlayers() {
        return (int) announcements.stream()
                .filter(announcement -> announcement.getPlayer().getPosition() == Position.FORWARD)
                .count();
    }
}

/**
 * Represents the position of a football player
 */
enum Position {
    FORWARD,
    MIDFIELD,
    DEFENSE,
    GOALKEEPER
}

/**
 * Represents a football player with personal and professional information
 */
class Player {
    private String name;
    private int age;
    private int number;
    private Position position;

    /**
     * Default constructor
     */
    public Player() {
    }

    // Getters and setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public Position getPosition() {
        return position;
    }

    public void setPosition(Position position) {
        this.position = position;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Player player = (Player) o;
        return age == player.age && number == player.number && 
               Objects.equals(name, player.name) && position == player.position;
    }

    @Override
    public int hashCode() {
        return Objects.hash(name, age, number, position);
    }
}

/**
 * Represents the type of announcement made during a football match
 */
enum AnnouncementType {
    SCORE,
    SAVE
}

/**
 * Represents an announcement made during a football match
 */
class Announcement {
    private Date time;
    private AnnouncementType type;
    private Player player;

    /**
     * Default constructor
     */
    public Announcement() {
    }

    // Getters and setters
    public Date getTime() {
        return time;
    }

    public void setTime(Date time) {
        this.time = time;
    }

    public AnnouncementType getType() {
        return type;
    }

    public void setType(AnnouncementType type) {
        this.type = type;
    }

    public Player getPlayer() {
        return player;
    }

    public void setPlayer(Player player) {
        this.player = player;
    }
}