/**
 * Represents a customer with personal information and rental details.
 */
class Customer {
    private String name;
    private String surname;
    private String address;
    private String rentedCarPlate;

    /**
     * Default constructor initializes an empty customer object.
     */
    public Customer() {
    }

    /**
     * Getter for name.
     *
     * @return Name of the customer
     */
    public String getName() {
        return name;
    }

    /**
     * Setter for name.
     *
     * @param name Name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * Getter for surname.
     *
     * @return Surname of the customer
     */
    public String getSurname() {
        return surname;
    }

    /**
     * Setter for surname.
     *
     * @param surname Surname to set
     */
    public void setSurname(String surname) {
        this.surname = surname;
    }

    /**
     * Getter for address.
     *
     * @return Address of the customer
     */
    public String getAddress() {
        return address;
    }

    /**
     * Setter for address.
     *
     * @param address Address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * Getter for rentedCarPlate.
     *
     * @return Plate of the rented car
     */
    public String getRentedCarPlate() {
        return rentedCarPlate;
    }

    /**
     * Setter for rentedCarPlate.
     *
     * @param rentedCarPlate Plate of the rented car to set
     */
    public void setRentedCarPlate(String rentedCarPlate) {
        this.rentedCarPlate = rentedCarPlate;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Customer customer = (Customer) o;
        return Objects.equals(name, customer.name) &&
                Objects.equals(surname, customer.surname) &&
                Objects.equals(address, customer.address) &&
                Objects.equals(rentedCarPlate, customer.rentedCarPlate);
    }

    @Override
    public int hashCode() {
        return Objects.hash(name, surname, address, rentedCarPlate);
    }
}

