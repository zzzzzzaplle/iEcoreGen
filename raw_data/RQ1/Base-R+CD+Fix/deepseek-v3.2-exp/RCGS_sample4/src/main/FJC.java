import java.util.*;
import java.util.stream.Collectors;

class Store {
    private List<Car> cars;
    private List<Rental> rentals;
    private List<OverdueNotice> notices;

    public Store() {
        this.cars = new ArrayList<>();
        this.rentals = new ArrayList<>();
        this.notices = new ArrayList<>();
    }

    public List<Car> getCars() {
        return cars;
    }

    public void setCars(List<Car> cars) {
        this.cars = cars;
    }

    public List<Rental> getRentals() {
        return rentals;
    }

    public void setRentals(List<Rental> rentals) {
        this.rentals = rentals;
    }

    public List<OverdueNotice> getNotices() {
        return notices;
    }

    public void setNotices(List<OverdueNotice> notices) {
        this.notices = notices;
    }

    /**
     * Identifies available cars in the store. A car is available if it is not currently rented.
     * Returns a list of available cars sorted by daily price in ascending order.
     * If no available cars exist, returns an empty list.
     *
     * @return List of available cars sorted by daily price ascending, or empty list if none available
     */
    public List<Car> identifyAvailableCars() {
        Set<Car> rentedCars = rentals.stream()
                .filter(rental -> rental.getBackDate() == null)
                .map(Rental::getCar)
                .collect(Collectors.toSet());
        
        return cars.stream()
                .filter(car -> !rentedCars.contains(car))
                .sorted(Comparator.comparingDouble(Car::getDailyPrice))
                .collect(Collectors.toList());
    }

    /**
     * Calculates the total revenue generated by all rentals in the store.
     * Sums up the totalPrice of all rentals where the car has been returned (backDate is not null).
     *
     * @return Total revenue from all completed rentals
     */
    public double calculateTotalRevenue() {
        return rentals.stream()
                .filter(rental -> rental.getBackDate() != null)
                .mapToDouble(Rental::getTotalPrice)
                .sum();
    }

    /**
     * Lists customers with overdue rentals. A rental is overdue if the back date is null
     * and the current date is past the due date.
     *
     * @param currentDate The current date to check against due dates
     * @return List of customers with overdue rentals, or empty list if none exist
     */
    public List<Customer> findCustomersWithOverdueRentals(Date currentDate) {
        return rentals.stream()
                .filter(rental -> rental.getBackDate() == null && currentDate.after(rental.getDueDate()))
                .map(Rental::getCustomer)
                .distinct()
                .collect(Collectors.toList());
    }

    /**
     * Determines the average daily price of cars in the store.
     * Sums the daily prices of all cars and divides by the total number of cars.
     * If the store has no cars, returns 0.0.
     *
     * @return Average daily price of all cars, or 0.0 if no cars exist
     */
    public double determineAverageDailyPrice() {
        if (cars.isEmpty()) {
            return 0.0;
        }
        return cars.stream()
                .mapToDouble(Car::getDailyPrice)
                .average()
                .orElse(0.0);
    }

    /**
     * Counts the number of cars rented per customer.
     * Returns a map where keys are customers and values are their respective rental counts.
     *
     * @return Map of customers and their rental counts, or empty map if no rentals exist
     */
    public Map<Customer, Integer> countCarsRentedPerCustomer() {
        return rentals.stream()
                .collect(Collectors.groupingBy(
                    Rental::getCustomer,
                    Collectors.collectingAndThen(Collectors.counting(), Long::intValue)
                ));
    }
}

