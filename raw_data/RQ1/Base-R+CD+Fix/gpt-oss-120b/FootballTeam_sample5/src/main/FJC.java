import java.util.ArrayList;
import java.util.Date;
import java.util.List;
import java.util.Objects;

/**
 * Represents a football team with its players, first eleven, spare team and
 * announcements generated during matches.
 */
 class FootballTeam {

    private List<Player> firstEleven;
    private List<Player> spareTeam;
    private List<Player> players;
    private List<Announcement> announcements;

    /** Unparameterized constructor. Initializes internal collections. */
    public FootballTeam() {
        this.firstEleven = new ArrayList<>();
        this.spareTeam = new ArrayList<>();
        this.players = new ArrayList<>();
        this.announcements = new ArrayList<>();
    }

    /* --------------------------------------------------------------------- */
    /* Getters and Setters                                                   */
    /* --------------------------------------------------------------------- */

    public List<Player> getFirstEleven() {
        return firstEleven;
    }

    public void setFirstEleven(List<Player> firstEleven) {
        this.firstEleven = firstEleven;
    }

    public List<Player> getSpareTeam() {
        return spareTeam;
    }

    public void setSpareTeam(List<Player> spareTeam) {
        this.spareTeam = spareTeam;
    }

    public List<Player> getPlayers() {
        return players;
    }

    public void setPlayers(List<Player> players) {
        this.players = players;
    }

    public List<Announcement> getAnnouncements() {
        return announcements;
    }

    public void setAnnouncements(List<Announcement> announcements) {
        this.announcements = announcements;
    }

    /* --------------------------------------------------------------------- */
    /* Helper methods for managing collections                               */
    /* --------------------------------------------------------------------- */

    /**
     * Adds a player to the overall player list.
     *
     * @param player the player to add
     */
    public void addPlayer(Player player) {
        if (player != null) {
            this.players.add(player);
        }
    }

    /**
     * Adds a player to the first eleven list.
     *
     * @param player the player to add
     */
    public void addToFirstEleven(Player player) {
        if (player != null && this.firstEleven.size() < 11) {
            this.firstEleven.add(player);
        }
    }

    /**
     * Adds a player to the spare team list.
     *
     * @param player the player to add
     */
    public void addToSpareTeam(Player player) {
        if (player != null) {
            this.spareTeam.add(player);
        }
    }

    /**
     * Adds an announcement to the team.
     *
     * @param announcement the announcement to add
     */
    public void addAnnouncement(Announcement announcement) {
        if (announcement != null) {
            this.announcements.add(announcement);
        }
    }

    /* --------------------------------------------------------------------- */
    /* Functional requirements implementations                               */
    /* --------------------------------------------------------------------- */

    /**
     * Calculates the total number of goal‑scoring announcements generated by
     * the first eleven players of this team. Only players whose position is
     * {@link Position#FORWARD} are considered, because only forwards can
     * generate score announcements.
     *
     * @return the count of score announcements belonging to the first eleven
     */
    public int calculateTotalGoalScoringAnnouncementsForFirstEleven() {
        int count = 0;
        for (Announcement a : announcements) {
            if (a.getType() == AnnouncementType.SCORE) {
                Player p = a.getPlayer();
                if (p != null
                        && p.getPosition() == Position.FORWARD
                        && firstEleven.contains(p)) {
                    count++;
                }
            }
        }
        return count;
    }

    /**
     * Finds the midfielder with the highest jersey number among all players
     * of this team.
     *
     * @return the midfielder player with the highest number, or {@code null}
     *         if no midfielder exists in the team
     */
    public Player findMidfielderWithHighestNumber() {
        Player result = null;
        for (Player p : players) {
            if (p.getPosition() == Position.MIDFIELD) {
                if (result == null || p.getNumber() > result.getNumber()) {
                    result = p;
                }
            }
        }
        return result;
    }

    /**
     * Calculates the average age of the players that belong to the spare
     * (reserve) team.
     *
     * @return the average age as a {@code double}; returns {@code 0.0}
     *         when the spare team is empty
     */
    public double calculateAverageAgeOfSpareTeam() {
        if (spareTeam.isEmpty()) {
            return 0.0;
        }
        int totalAge = 0;
        for (Player p : spareTeam) {
            totalAge += p.getAge();
        }
        return (double) totalAge / spareTeam.size();
    }

    /**
     * Counts the number of goal‑saving announcements made by all goalkeepers
     * on the team. Only announcements whose type is {@link AnnouncementType#SAVE}
     * and whose player position is {@link Position#GOALKEEPER} are counted.
     *
     * @return the count of save announcements generated by goalkeepers
     */
    public int calculateGoalSavingAnnouncementsByGoalkeeper() {
        int count = 0;
        for (Announcement a : announcements) {
            if (a.getType() == AnnouncementType.SAVE) {
                Player p = a.getPlayer();
                if (p != null && p.getPosition() == Position.GOALKEEPER) {
                    count++;
                }
            }
        }
        return count;
    }

    /**
     * Calculates the total number of announcements (of any type) generated by
     * all forward players on the team.
     *
     * @return the total number of announcements made by forwards; returns
     *         {@code 0} when there are no forwards or no announcements
     */
    public int calculateTotalAnnouncementsForForwardPlayers() {
        int count = 0;
        for (Announcement a : announcements) {
            Player p = a.getPlayer();
            if (p != null && p.getPosition() == Position.FORWARD) {
                count++;
            }
        }
        return count;
    }
}

/* ------------------------------------------------------------------------- */
/* Supporting domain classes and enums                                       */
/* ------------------------------------------------------------------------- */

/**
 * Represents a football player.
 */
class Player {

    private String name;
    private int age;
    private int number;
    private Position position;

    /** Unparameterized constructor. */
    public Player() {
        // default values (null / 0) are acceptable
    }

    /* Getters and Setters */

    public String getName() {
        return name;
    }

    /**
     * Sets the player's name.
     *
     * @param name the name to assign
     */
    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    /**
     * Sets the player's age.
     *
     * @param age the age in years
     */
    public void setAge(int age) {
        this.age = age;
    }

    public int getNumber() {
        return number;
    }

    /**
     * Sets the player's jersey number.
     *
     * @param number the jersey number
     */
    public void setNumber(int number) {
        this.number = number;
    }

    public Position getPosition() {
        return position;
    }

    /**
     * Sets the player's position on the pitch.
     *
     * @param position the position enum value
     */
    public void setPosition(Position position) {
        this.position = position;
    }

    /* Optional: equals & hashCode to allow List.contains checks */

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        Player player = (Player) o;
        return number == player.number &&
                Objects.equals(name, player.name);
    }

    @Override
    public int hashCode() {
        return Objects.hash(name, number);
    }
}

/**
 * Enumeration of possible player positions.
 */
enum Position {
    FORWARD,
    MIDFIELD,
    DEFENSE,
    GOALKEEPER
}

/**
 * Represents an announcement made during a match (e.g., a goal scored or saved).
 */
class Announcement {

    private Date time;
    private AnnouncementType type;
    private Player player;

    /** Unparameterized constructor. */
    public Announcement() {
        // default values (null) are acceptable
    }

    /* Getters and Setters */

    public Date getTime() {
        return time;
    }

    /**
     * Sets the time of the announcement.
     *
     * @param time the {@link Date} when the announcement occurred
     */
    public void setTime(Date time) {
        this.time = time;
    }

    public AnnouncementType getType() {
        return type;
    }

    /**
     * Sets the type of the announcement.
     *
     * @param type the {@link AnnouncementType} (SCORE or SAVE)
     */
    public void setType(AnnouncementType type) {
        this.type = type;
    }

    public Player getPlayer() {
        return player;
    }

    /**
     * Sets the player related to this announcement.
     *
     * @param player the {@link Player} who generated the announcement
     */
    public void setPlayer(Player player) {
        this.player = player;
    }
}

/**
 * Types of announcements that can be generated during a match.
 */
enum AnnouncementType {
    SCORE,
    SAVE
}